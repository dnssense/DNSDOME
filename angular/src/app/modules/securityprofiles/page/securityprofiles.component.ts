    hideWizard() {
        this.alert.alertWarningAndCancel('Are You Sure?', 'Your Changes will be cancelled!').subscribe(
            res => {
                if (res) {
                    $('#wizardPanel').hide("slide", { direction: "right" }, 1000);
                    $('#securityProfilesPanel').show("slide", { direction: "left" }, 1000);
                    //refresh models 
                }
            }
        );
    }


