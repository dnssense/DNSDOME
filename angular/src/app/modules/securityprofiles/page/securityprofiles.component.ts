        if (id) {
            $('#securityProfilesPanel').toggle("slide", { direction: "left" }, 600);
            $('#wizardPanel').toggle("slide", { direction: "right" }, 600);
            this.installWizard();
            document.getElementById('wizardPanel').scrollIntoView();
        }
